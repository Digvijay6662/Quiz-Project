    private void jcStudentIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcStudentIdActionPerformed
        if (studentIdNotSelected) {
            studentIdNotSelected = false;
            return;
        }
        try {
            ArrayList<String> examIdList = PerformanceDAO.getAllExamId(jcStudentId.getSelectedItem().toString());
            jcExamId.removeAllItems();
            for (String s : examIdList) {
                jcExamId.addItem(s);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, " QuestionDAO < error connection to DB > " + ex, "Exception!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcStudentIdActionPerformed

    private void jcExamIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcExamIdActionPerformed
        if (examIdNotSelected) {
            examIdNotSelected = false;
            return;
        }
        try {
            String studentid = jcStudentId.getSelectedItem().toString();
            String examid = jcExamId.getSelectedItem().toString();

            StudentScore studentScore = PerformanceDAO.getScore(studentid, examid);

            txtLanguage.setText(studentScore.getLanguage());
            txtPercentage.setText(String.valueOf(studentScore.getPer()));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, " QuestionDAO < error connection to DB > " + ex, "Exception!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcExamIdActionPerformed

