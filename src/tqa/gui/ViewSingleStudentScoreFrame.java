    public void loadStudentId() {
        try {
            ArrayList<String> studentIdList = PerformanceDAO.getAllStudentId();
            if (studentIdList.isEmpty()) {
                JOptionPane.showMessageDialog(null, " Sorry ! No Student Has Not Yet Appread ", "OOPs!!", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                jcStudentId.removeAllItems();
                for (String s : studentIdList) {
                    jcStudentId.addItem(s);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, " QuestionDAO < error connection to DB > " + ex, "Exception!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    public void customCursor() {
        Toolkit toolkit = Toolkit.getDefaultToolkit();
        Image img = toolkit.getImage(getClass().getResource("../images/MangekyouCursor.png"));
        Point point = new Point(0, 0);
        Cursor cursor = toolkit.createCustomCursor(img, point, "MangekyouCursor");
        this.setCursor(cursor);
    }

