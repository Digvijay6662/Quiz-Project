    public int validateInput() {
        if (txtPassword.getPassword().length == 0 || txtCPassword.getPassword().length == 0) {
            return 0;
        } else if (txtPassword.getPassword().length < 4) {
            return -2;
        } else if (String.valueOf(txtPassword.getPassword()).equals(String.valueOf(txtCPassword.getPassword())) == false) {
            return -1;
        }
        return 1;
    }

