    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (validateInput() == -2) {
            JOptionPane.showMessageDialog(null, " Password Should Be More Than 4 Characters ", "ERROR!!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (validateInput() == -1) {
            JOptionPane.showMessageDialog(null, " Password And Retype Password Didn't Match ", "ERROR!!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (validateInput() == 0) {
            JOptionPane.showMessageDialog(null, " Please Fill All The Fields ", "ERROR!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            password = String.valueOf(txtPassword.getPassword());
            boolean success = UserDAO.updateUser(userid, password);
            if (success) {
                JOptionPane.showMessageDialog(null, " Passwordd Successfully Updated ", "HUUURRAYYYY!!", JOptionPane.INFORMATION_MESSAGE);
                return;
            } else {
                JOptionPane.showMessageDialog(null, " Password Not Updated ", "ERROR!!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, " UserDAO < error connection to DB > " + ex, "Exception!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

